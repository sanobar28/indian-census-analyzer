package model;

public class IndiaStateCodeCSV {
	int serialNumber;
	String stateName;
	int tin;
	String stateCode;
	
	public IndiaStateCodeCSV(int serialNumber, String stateName, int tin, String stateCode) {
		this.serialNumber = serialNumber;
		this.stateName = stateName;
		this.tin = tin;
		this.stateCode = stateCode;
	}
	
    
}